def format_name(f_name, l_name):
    formato_first = (f_name.title()) #TITLE TRANFORMA TUDO PARA A PRIMEIRA LETRA MAIUSCULA E AS DEMAIS NINUSCULAS.
    formato_last = (l_name.title())
    return f"{formato_first} {formato_last}"

